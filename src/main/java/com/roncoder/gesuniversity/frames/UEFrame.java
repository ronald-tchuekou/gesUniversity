    private void setFiliereComboBox() {
        DBManager db = new DBManager();
        try {
            List<String> fac = new ArrayList<>();
            db.select("filieres", new String[]{"*"});
            ResultSet result = db.get();
            while (result.next()) {
                fac.add(result.getString("code"));
            }
            filiere.setModel(new javax.swing.DefaultComboBoxModel<>(fac.toArray(new String[]{})));
            filiere.setSelectedIndex(0);
        } catch (SQLException ex) {
            Logger.getLogger(Dashboard.class.getName()).log(Level.SEVERE, null, ex);
        }
    }

