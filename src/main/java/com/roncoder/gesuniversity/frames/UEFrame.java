    private void btn_submitActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btn_submitActionPerformed
        // Save element to the database. and notify the table.
        String code_str = this.code.getText();
        String libelle_str = this.libelle.getText();
        String filiere_str = (String) this.filiere.getSelectedItem();
        String category_str = (String) this.category.getSelectedItem();
        int nb_credit_str = 0, vol_cm_str = 0, vol_td_str = 0, vol_tp_str = 0;
        try {
            nb_credit_str = Integer.parseInt(this.nb_credit.getText());
            vol_cm_str = Integer.parseInt(this.vol_cm.getText());
            vol_td_str = Integer.parseInt(this.vol_td.getText());
            vol_tp_str = Integer.parseInt(this.vol_tp.getText());
        } catch (java.lang.NumberFormatException ex) {
        } finally {
            // Validation.
            if (requiredFields()) {
                if (ue == null) {
                    dashboard.addUE(new UE(code_str, filiere_str, libelle_str, nb_credit_str,
                            vol_cm_str, vol_td_str, vol_tp_str, category_str));
                } else {
                    if (requiredFieldsNotOld()) {
                        ue.setLibelle(libelle_str);
                        ue.setNb_credit(nb_credit_str);
                        ue.setVol_cm(vol_cm_str);
                        ue.setVol_td(vol_td_str);
                        ue.setVol_tp(vol_tp_str);
                        ue.setCategory(category_str);
                        dashboard.updateUE(ue);
                    }
                }
                this.setVisible(false);
            }
        }
    }//GEN-LAST:event_btn_submitActionPerformed

    private boolean requiredFields() {
        if (code.getText().trim().equals("")) {
            JOptionPane.showMessageDialog(null, "Veuillez indiquer le code de la matière.");
            return false;
        }
        if (libelle.getText().trim().equals("")) {
            JOptionPane.showMessageDialog(null, "Veuillez indiquer le libelle de la matière.");
            return false;
        }
        if (nb_credit.getText().trim().equals("")) {
            JOptionPane.showMessageDialog(null, "Veuillez indiquer le nombre de crédit de la matière.");
            return false;
        }
        if (vol_cm.getText().trim().equals("")
                || vol_tp.getText().trim().equals("")
                || vol_td.getText().trim().equals("")) {
            JOptionPane.showMessageDialog(null, "Veuillez indiquer le volume horaire de la matière.");
            return false;
        }
        return true;
    }

    private boolean requiredFieldsNotOld() {
        // Check if the fields are different.
        if (code.getText().trim().equals(ue.getCode())
                && libelle.getText().trim().equals(ue.getLibelle())
                && filiere.getSelectedItem().equals(ue.getFiliere())
                && category.getSelectedItem().equals(ue.getCategory())
                && nb_credit.getText().trim().equals(String.valueOf(ue.getNb_credit()))
                && vol_cm.getText().trim().equals(String.valueOf(ue.getVol_cm()))
                && vol_td.getText().trim().equals(String.valueOf(ue.getVol_td()))
                && vol_tp.getText().trim().equals(String.valueOf(ue.getVol_tp()))) {
            JOptionPane.showMessageDialog(null, "Aucune modification apportée.");
            return false;
        }
        return true;
    }

