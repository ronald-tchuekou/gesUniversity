    private void btn_submitActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btn_submitActionPerformed
        // Save element to the database. and notify the table.
        String code_str = this.code.getText();
        String libelle_str = this.libelle.getText();
        String vol_cm_str = this.vol_h_cm.getText() + " H";
        String vol_td_str = this.vol_h_td.getText() + " H";
        String vol_tp_str = this.vol_h_tp.getText() + " H";
        String category_str = (String) this.category.getSelectedItem();
        int nb_credit_str = 0;
        try {
            nb_credit_str = Integer.parseInt(this.nb_credit.getText());
        } catch (java.lang.NumberFormatException ex) {
        } finally {
            // Validation.
            if (requiredFields()) {
                if (matiere == null) {
                    dashboard.addMatiere(new Matiere(code_str, libelle_str, nb_credit_str,
                            vol_cm_str, vol_td_str, vol_tp_str, category_str));
                } else {
                    if (requiredFieldsNotOld()) {
                        matiere.setLibelle(libelle_str);
                        matiere.setNb_credit(nb_credit_str);
                        matiere.setVol_cm(vol_cm_str);
                        matiere.setVol_td(vol_td_str);
                        matiere.setVol_tp(vol_tp_str);
                        matiere.setCategory(category_str);
                        dashboard.updateMatiere(matiere);
                    }
                }
                this.setVisible(false);
            }
        }
    }//GEN-LAST:event_btn_submitActionPerformed

    private boolean requiredFieldsNotOld() {
        // Check if the fields are different.
        if (code.getText().trim().equals(matiere.getCode())
                && libelle.getText().trim().equals(matiere.getLibelle())
                && category.getSelectedItem().equals(matiere.getCategory())
                && nb_credit.getText().trim().equals(String.valueOf(matiere.getNb_credit()))
                && matiere.getVol_cm().equals(vol_h_cm.getText().trim() + " H")
                && matiere.getVol_td().equals(vol_h_td.getText().trim() + " H")
                && matiere.getVol_tp().equals(vol_h_tp.getText().trim() + " H")) {
            JOptionPane.showMessageDialog(null, "Aucune modification apport√©e.");
            return false;
        }
        return true;
    }

    private boolean requiredFields() {
        if (code.getText().trim().equals("")) {
            JOptionPane.showMessageDialog(null, "Veuillez indiquer le code de la mati√®re.");
            return false;
        }
        if (libelle.getText().trim().equals("")) {
            JOptionPane.showMessageDialog(null, "Veuillez indiquer le libelle de la mati√®re.");
            return false;
        }
        if (nb_credit.getText().trim().equals("")) {
            JOptionPane.showMessageDialog(null, "Veuillez indiquer le nombre de cr√©dit de la mati√®re.");
            return false;
        }
        if (vol_h_cm.getText().trim().equals("")
                || vol_h_tp.getText().trim().equals("")
                || vol_h_td.getText().trim().equals("")) {
            JOptionPane.showMessageDialog(null, "Veuillez indiquer le volume horaire de la mati√®re.");
            return false;
        }
        return true;
    }

